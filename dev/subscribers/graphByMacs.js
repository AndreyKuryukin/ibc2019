const moment = require('moment');


const actualize = (value, index) => {
    value.date_time = moment().startOf('day').subtract(index, 'hours').startOf('hour').toISOString();
    return value
};

const replaceMac = (mac) => (value) => ({ ...value, id: mac });

const decreaseKab = (decrement) => (value) => _.reduce(value, (result, val, key) => {
    if (key === 'date_time' || key === 'id') {
        result[key] = val;
    } else if (_.isNumber(val)) {
        result[key] = val - decrement;
    } else {
        result[key] = val;
    }
    return result;
}, {});

const randomize = (weight) => (value) => _.reduce(value, (result, val, key) => {
    if (key === 'date_time' || key === 'id') {
        result[key] = val;
    } else if (_.isNumber(val)) {
        const delta = 100 - val;
        result[key] = Math.abs(val + delta * Math.sin(delta * weight) - weight);
    } else {
        result[key] = Math.abs(val);
    }
    return result;
}, {});

